     * @see    HTML_QuickForm2_Container::getElementByName()
     */
    public function getElementsByName($name)
    {
        $root      = $this->getRoot();
        $container =& $root->_ids;

        $found = array();
        $name  = $this->clean((string) $name);

        foreach ($container as $element) {
            if ($element->getName() === $name) {
