    public function isRoot()
    {
        if ($this->_isRootElement) {
            $root = true;
        } elseif ($this->_html === null && $this->_parent === null) {
            $root = true;
        } elseif ($this->_html !== null && is_object($this->_html)) {
            $root = false;
        } elseif ($this->_parent !== null && is_object($this->_parent)) {
            $root = false;
        } else {
            $root = true;
        }

        return $root;
    }

