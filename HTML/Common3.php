     *
     * @param string $subject string to format
     *
     * @return string
     * @access protected
     */
    protected function collapseInternalSpaces($subject)
    {
        //delete tabulators
        $string = preg_replace('/\t+/', ' ', (string) $subject);
        //delete spaces
        $string = preg_replace('/\s+/', ' ', (string) $subject);
        return $string;
