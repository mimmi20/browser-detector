    protected function storeId($id, $element = null)
    {
        if ($id == '' || $id === null) {
            return false;
        }

        $id = (string) $id;
        if ($element === null) {
            $element =& $this;
        }

        if ($this->registerId($id) === true) {
            $root      = $this->getRoot();
            $container =& $root->_ids;

            if (!isset($container[$id])) {
                $container[$id] =& $element;
                return true;
            } elseif (isset($container[$id]) &&
